// 1 以上 N 以下の整数が素数かどうかを返す
vector<bool> Eratosthenes(int N) {
    // テーブル
    vector<bool> isprime(N+1, true);

    // 0, 1 は予めふるい落としておく
    isprime[0] = isprime[1] = false;

    // ふるい
    for (int p = 2; p <= N; ++p) {
        // すでに合成数であるものはスキップする
        if (!isprime[p]) continue;

        // p 以外の p の倍数から素数ラベルを剥奪
        for (int q = p * 2; q <= N; q += p) {
            isprime[q] = false;
        }
    }

    // 1 以上 N 以下の整数が素数かどうか
    return isprime;
}

int main(){
    int N, K;
    cin >> N >> K;
    auto isprime = Eratosthenes(N);
    vi factornum(N+1, 0);
    for (int i = 2; i <= N; ++i) {
        if (isprime[i]) {
            for (int j = i; j <= N; j += i) {
                ++factornum[j];
            }
        }
    }
    int ans{0};
    for (const auto& f : factornum) {
        if (f >= K) ++ans;
    }
    cout << ans << endl;
}