S = input()
d = defaultdict(lambda:ModInt(0))
a = ord('A')
W = 2**10
for s in S:
    s = ord(s) - a
    for i in range(s*W,(s+1)*W):
        d[i] *= 2
    for bit in range(1<<10):
        if bit >> s & 1:
            continue
        for j in range(10):
            d[s*W+(bit | (1 << s))] += d[j*W+bit]
    d[s*W+(1<<s)] += 1
ans = 0
